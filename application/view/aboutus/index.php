			<p>SOS Rommelmarkt is onderdeel van de Bondgenoten Sociale Activering (BOSA). In ’s-Hertogenbosch leiden honderden inwoners een bestaan aan de rand van de samenleving. Ze hebben doorgaans geen werk of zelfs geen vaste verblijfplaats. Ze hebben een gevangenisstraf achter de rug en kunnen maar met moeite de draad weer oppakken. Ze kampen met gezondheidsproblemen, vaak als gevolg van drugsgebruik. Ze hebben een chronisch gebrek aan inkomen, omdat ze niet voldoen aan de voorwaarden voor een bijstandsuitkering. Meestal gaat het om een combinatie van problemen.</p>
			</br>
			<p>Sinds 2004 werken in ’s-Hertogenbosch een aantal organisaties samen in BOSA, Bondgenoten Sociale Activering. De BOSA-organisaties beginnen waar de gangbare hulpverleningsinstellingen het contact hebben verloren. Zonder veel voorwaarden, wachtlijsten of bureaucratie. Door gewoon te kijken wat iemand kan en wil, op eigen kracht; met een steuntje in de rug, maar zonder dwang. Door open en eerlijk te zijn, en niet te overvragen</p>
			</br>
			<p>De BOSA-organisaties zijn op de eerste plaats een veilige haven, waar mensen hun verhaal kunnen doen, even op adem komen, samen met anderen iets doen en ook nog wat bijverdienen. Voor velen is dat het begin van een nieuw leven. Dankzij het respect van medewerkers en talrijke vrijwilligers ontwikkelt zich (weer) zelfvertrouwen. Arbeid en dagbesteding brengen structuur in het dagelijks leven. Soms vindt iemand zelfs een baan; de meesten zetten kleinere stappen.</p>

			
